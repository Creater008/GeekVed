</td>
</tr>
<tr>
<td>
  <p>The word 'robotics' was first used in Runaround, a short story published in 1942, by Isaac Asimov (born Jan. 2, 1920, died Apr. 6, 1992). I, Robot, a collection of several of these stories, was published in 1950.
    
    One of the first robots Asimov wrote about was a robotherapist. A modern counterpart to Asimov's fictional character is Eliza. Eliza was born in 1966 by a Massachusetts Institute of Technology Professor Joseph Weizenbaum who wrote Eliza -- a computer program for the study of natural language communication between man and machine.
    
    She was initially programmed with 240 lines of code to simulate a psychotherapist by answering questions with questions.</p>
