<td bgcolor="#7e7f80"><font color="#fff">DESOLDRING</font></td>
</tr>
<tr>
<td><p>In electronics, desoldering is the removal of solder and components from a circuit for troubleshooting, for repair  purposes, component replacement, and to salvage components. Electronic  components are often mounted on a circuit board, and it is  usually desirable to avoid damaging the circuit board, surrounding components,  and the component being removed.<br>
Specialized tools, materials, and techniques have been devised to aid in  the desoldering process.</p>
  <p>&nbsp;</p></td>
</tr>
<tr>
<td bgcolor="#7e7f80">
<font color="#fff">TOOLS</font></td>
</tr>
<tr>
<td><p>Desoldering tools and materials include the following:</p>
  <ul type="disc">
    <li>Solder       wick</li>
    <li>Heat guns, also       called hot air guns</li>
    <li>Desoldering       pump</li>
    <li>Removal       alloys</li>
    <li>Removal       fluxes</li>
    <li>Heated       soldering tweezers</li>
    <li>Various       picks and tweezers for tasks such as pulling at, holding, removing, and       scraping components.</li>
    <li>Vacuum       and pressure pumps with specialized heater tips and nozzles</li>
    <li>Rework       stations, used to repair printed circuit board assemblies that fail       factory test.</li>
  </ul>
  <p>Terminology is not totally standardized. Anything with a base unit with  provision to maintain a stable temperature, pump air in either direction, etc.,  is often called a &quot;station&quot; (preceded by rework, soldering,  desoldering, hot air); one, or sometimes more, tools may be connected to a  station, e.g., a rework station may accommodate a soldering iron and hot air  head. A soldering iron with a hollow tip and a spring-, bulb-, or  electrically-operated suction pump may be called a desoldering iron. Terms such  as &ldquo;suction pen&rdquo; may be used; the meaning is usually clear from the context.</p>
  <p>&nbsp;</p></td>
</tr>
<tr>
<td bgcolor="#7e7f80"><font color="#fff">PUMPS</font></td>
</tr>
<tr>
<td><p>Electrically operated pumps are used for several purposes in  conjunction with a hand-held head connected by a tube.<br>
Suction pumps are used to suck away molten solder, leaving previously  joined terminals disconnected. They are primarily used to release through-hole connections  from a PCB. The desoldering head must be designed so that the extracted solder  does not solidify so as to obstruct it, or enter the pump, and can be removed  and discarded easily. It is not possible to remove a multi-pin part by melting  solder on the pins sequentially, as one joint will solidify as the next is  melted; pumps and solder wick are among methods to remove solder from all  joints, leaving the part free to be removed.<br>
Suction pumps are also used with a suction head appropriate for each  part to pick up and remove tiny surface mount devices once solder has melted,  and to place parts.<br>
Hot air pumps blow air hot enough to melt all the solder around a small  surface mounted part, and can be used for soldering parts in place, and for  desoldering followed by removal before the solder solidifies by a vacuum pump  or with tweezers. Hot air has a tendency to oxidise metals; a  non-oxidising gas, usually nitrogen, can be  used instead of air, at increased cost of equipment and consumables.<br>
A <em>desoldering pump</em>, colloquially known as a <em>solder sucker</em>,  is a manually-operated device which is used to remove solder from a printed circuit board. There are two types: the <em>plunger</em> style and <em>bulb</em> style. (An electrically-operated pump for this purpose  would usually be called a vacuum pump.)<br>
The plunger type has a cylinder with a spring-loaded piston which is  pushed down and locks into place. When triggered by pressing a button, the  piston springs up, creating suction that <br>
sucks the solder off the soldered connection. The bulb type creates  suction by squeezing and releasing a rubber bulb.<br>
The pump is applied to a heated solder connection, then operated to suck  the solder away. It is sometimes considered that spring-operated pumps are more <br>
likely to cause damage than bulbs or electrically-operated pumps due to  their jarring mechanical recoil.</p>
  <p>&nbsp;</p></td>
</tr>
<tr>
<td bgcolor="#7e7f80"><font color="#fff">SOLDER WICK</font></td>
</tr>
<tr>
  <td><em>Solder wick</em>, also known as <em>desoldering wick</em> or <em>desoldering  braid</em>, is fine, braided 18 to 42 AWG copper wire coated  with rosin flux, usually  supplied on a roll.<br>
    The end of a length of braid is positioned over the solder to be removed  and the portion in contact with the joint is heated with a soldering iron; as it is heated, the solder melts and is wicked  into the braid by capillary action. The braid  is then removed while the solder is still molten, and the used part cut off and  discarded when cool. Short cut lengths of braid may be used to prevent heat  being carried away by the braid instead of heating the joint.
    <p>&nbsp;</p></td>
</tr>
<tr>
<td bgcolor="#7e7f80"><font color="#fff">TECHNIQUE</font></td>
</tr>
<tr>
  <td><p>Desoldering requires application of heat to the solder joint and  removing the molten solder so that the joint may be separated. Desoldering may  be required to replace a defective component, to alter an existing circuit, or  to salvage components for re-use. Use of too high a temperature or heating for  too long may damage components or destroy the bond between a printed circuit trace and the board substrate. Techniques are  different for through-hole and surface-mounted components.</p>
    <p>&nbsp;</p></td>
</tr>
<tr>
<td bgcolor="#7e7f80"><font color="#fff">THROUGH - HOLE</font></td>
</tr>
<tr>
<td><p>A component with one or two connections to the PCB can usually be  removed by heating one joint, pulling out an end of the component while the  solder is molten (bending the other lead to do so), and repeating for the  second joint. Solder filling the hole can be removed with a pump or with a  pointed object made of a a material which solder does not wet, such as  stainless steel or wood.<br>
If a multi-pin component need not be salvaged, it is often possible to  cut the pins, then remove the residual ends one by one.<br>
Components with more connections cannot be removed intact in the way  described above unless the wire leads are long and flexible enough to be pulled  out one by one. For a component such as a Dual-Inline Package (DIP), the pins are too short to pull out, and  solder melted on one joint will solidify before another can be melted. A  technique sometimes used is the use of a large soldering-iron tip designed to  melt the solder on all pins at once; different tips are required for different  packages. The component is removed while the solder is molten, most easily by a  spring-loaded puller attached to it before heating.<br>
Otherwise all joints must be freed from solder before the component can  be removed. Each joint must be heated and the solder removed from it while  molten using a vacuum pump, manual desoldering pump, or desoldering braid.<br>
For through-hole mounted devices on double-sided or multi-layer boards,  special care must be taken not to remove the via connecting the layers, as this will ruin the  entire board. Hard pulling on a lead which is not entirely free of solder (or  with solder not thoroughly molten in the case of a soldering iron tip heating  all pins) may pull out a via.<br>
To remove and recover all components, both through-hole and  surface-mount, from a board which itself is usually no longer needed, a flame  or hot air gun can be used to rapidly heat all parts so they can be pulled off.  Parts may be damaged, and toxic fumes emitted, if excessive temperature or  prolonged heating is used.</p>
  <p>&nbsp;</p></td>
</tr>
<tr>
<td bgcolor="#7e7f80"><font color="#fff">SURFACE MOUNT</font></td>
</tr>
<tr>
<td><p>If they do not need to be re-used, some surface-mount components can be removed by cutting their  leads and desoldering the remnants with a soldering iron.<br>
If they may not be destroyed, surface-mount components must be removed  by heating the entire component to a temperature sufficient to melt the solder  used, but not high or prolonged enough to damage the component. For most  purposes a temperature not exceeding 260&deg;C for a time not exceeding 10 seconds  is acceptable.<br>
The entire board may be preheated to a temperature that all components  can withstand indefinitely. Then localised heat is applied to the component to  remove, with less heating required than from cold. The most usual tool is a hot  air (or hot gas) gun with a nozzle of appropriate size and shape to heat the  component, with nearby components shielded from the heat if necessary, followed  by removal with tweezers or a vacuum tool. Removal of multi-pin components with  a soldering iron and solder removal tools is impractical, as the solder between  the component and the pads remains in place, unlike solder which can be removed  from a hole.<br>
Hot air (or gas) may be applied with tools ranging from some portable  gas soldering irons such as the Weller Portasol  Professional which can be fitted with a narrow hot-air nozzle, set to a  temperature not controlled but approximately correct, to an industrial rework  station with many facilities including hot-gas blowing, vacuum part holding, soldering  iron head, and nozzles and fitting specific to particular component packages.</p>
